package br.com.fiap.estoque;


public class EstoqueBasico {

	public int soma(int nr1, int nr2) {
		return(nr1+nr2);
	}
	public double soma(double nr1,double nr2) {
		return (nr1+nr2);
	}
		
	public double mult(double nr1, double nr2) {
		return nr1*nr2;
	}
	
	public double div(double nr1, double nr2) {
		return nr1/nr2;
	}
	
	public double sub(double nr1, double nr2) {
		return nr1 - nr2;
	}
	
}
